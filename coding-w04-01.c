int main() { // int : Keyword, main : identifier, () : Operator
    printf("Hello,\nC Tokens!\n"); // printf : identifier, "Hello" : Constant, \n : newline, ';' : Operator
    return 0; // return : Keyword, 0 : Constant ,  ; : Operator 
}
    // Token Type อยู่ในโค้ด
    // Keyword: int, return
    // Identifier: main, printf
    // Operator: (), ;, =
    // Constant: "Hello", "C Tokens!", 0
