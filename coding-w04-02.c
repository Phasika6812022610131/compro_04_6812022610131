#include <stdio.h> // เรียกฟังก์ชัน printf() จาก standard I/O library

int main() {
    int a = 10; // int : Keyword, a : identifier, = : Operator, 10 : Constant
    printf("Value of a: %d\n", a); // printf : identifier, "Value of a: %d\n" : Format string, %d : Format specifier, a : Variable
    return 0; // return : Keyword, 0 : Constant 
}